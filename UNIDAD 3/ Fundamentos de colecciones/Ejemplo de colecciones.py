# Lista: Colección ordenada y mutable
lista_ejemplo = [10, 20, 30, "cuarenta"]

# Diccionario: Colección desordenada de pares clave-valor
diccionario_ejemplo = {"ciudad": "JOYA DE LOS SACHAS", "poblacion": 3000000}

# Conjunto: Colección desordenada y sin elementos duplicados
conjunto_ejemplo = {5, 10, 15, 20}

# Tupla: Colección ordenada e inmutable
tupla_ejemplo = (100, "cien", 200.0)

# Impresión en pantalla
print("Lista:", lista_ejemplo)
print("Diccionario:", diccionario_ejemplo)
print("Conjunto:", conjunto_ejemplo)
print("Tupla:", tupla_ejemplo)
